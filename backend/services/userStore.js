function buildCaseUpdateSql(tableName, columnName, idMappings) {
  const cases = [];
  const params = [];
  const whereIds = [];

  for (const [sourceId, targetId] of idMappings.entries()) {
    cases.push("WHEN ? THEN ?");
    params.push(Number(sourceId), Number(targetId));
    whereIds.push(Number(sourceId));
  }

  if (!cases.length) {
    return null;
  }

  const placeholders = whereIds.map(() => "?").join(", ");
  return {
    sql: `
      UPDATE ${tableName}
      SET ${columnName} = CASE ${columnName}
        ${cases.join(" ")}
        ELSE ${columnName}
      END
      WHERE ${columnName} IN (${placeholders})
    `,
    params: [...params, ...whereIds],
  };
}

async function remapSqliteUserReferences(idMappings) {
  if (!idMappings.size) {
    return;
  }

  const referenceTargets = [
    ["solicitudes", "solicitante_id"],
    ["solicitudes", "reviewed_by"],
    ["solicitudes", "dispatched_by"],
    ["solicitudes", "received_by"],
    ["solicitud_historial", "actor_id"],
    ["solicitud_mensajes", "remitente_id"],
    ["solicitud_mensajes", "destinatario_id"],
    ["inventario_movimientos", "actor_id"],
    ["envios_stock", "solicitado_por"],
    ["envios_stock", "autorizado_por"],
    ["notificaciones", "usuario_destino_id"],
    ["usuarios", "archivado_por"],
  ];

  for (const [tableName, columnName] of referenceTargets) {
    const statement = buildCaseUpdateSql(tableName, columnName, idMappings);
    if (!statement) {
      continue;
    }
    await sqliteRun(statement.sql, statement.params);
  }
}

async function replaceSqliteUsers(users) {
  const includeLegacyPassword = await hasLegacyPasswordColumn();
  const existingUsers = await sqliteAll("SELECT id, email FROM usuarios");
  const targetIdByEmail = new Map(users.map((user) => [normalizeEmail(user.email), Number(user.id)]));
  const idMappings = new Map();

  for (const row of existingUsers) {
    const normalizedEmail = normalizeEmail(row.email);
    const targetId = targetIdByEmail.get(normalizedEmail);
    if (!targetId || Number(row.id) === Number(targetId)) {
      continue;
    }
    idMappings.set(Number(row.id), Number(targetId));
  }

  await remapSqliteUserReferences(idMappings);
  await sqliteRun("DELETE FROM usuarios");

  for (const user of users) {
    const insertSql = includeLegacyPassword
      ? `
          INSERT INTO usuarios (
            id,
            nombre,
            email,
            password_hash,
            password,
            rol,
            equipo_id,
            activo,
            archivado,
            fecha_archivado,
            archivado_por,
            fecha_creacion
          )
          VALUES (?, ?, ?, ?, ?, ?, ?, ?, ?, ?, ?, ?)
        `
      : `
          INSERT INTO usuarios (
            id,
            nombre,
            email,
            password_hash,
            rol,
            equipo_id,
            activo,
            archivado,
            fecha_archivado,
            archivado_por,
            fecha_creacion
          )
          VALUES (?, ?, ?, ?, ?, ?, ?, ?, ?, ?, ?)
        `;

    const insertParams = includeLegacyPassword
      ? [
          user.id,
          user.nombre,
          user.email,
          user.password_hash,
          user.password_hash,
          user.rol,
          user.equipo_id,
          user.activo ? 1 : 0,
          user.archivado ? 1 : 0,
          user.fecha_archivado,
          user.archivado_por,
          toSqliteDateTime(user.fecha_creacion) || user.fecha_creacion,
        ]
      : [
          user.id,
          user.nombre,
          user.email,
          user.password_hash,
          user.rol,
          user.equipo_id,
          user.activo ? 1 : 0,
          user.archivado ? 1 : 0,
          user.fecha_archivado,
          user.archivado_por,
          toSqliteDateTime(user.fecha_creacion) || user.fecha_creacion,
        ];

    await sqliteRun(insertSql, insertParams);
  }

  await sqliteRun(
    `
      UPDATE sqlite_sequence
      SET seq = ?
      WHERE name = 'usuarios'
    `,
    [Math.max(...users.map((user) => Number(user.id)), 0)]
  );
}

async function syncMirrorIfNeeded(force = false) {
  if (!PG_ENABLED) {
    return false;
  }

  const now = Date.now();
  if (!force && now - lastMirrorSyncAt < MIRROR_SYNC_TTL_MS) {
    return false;
  }

  const users = await fetchAllPgUsers();

  await sqliteRun("BEGIN IMMEDIATE TRANSACTION");
  try {
    await replaceSqliteUsers(users);
    await sqliteRun("COMMIT");
    lastMirrorSyncAt = now;
    return true;
  } catch (error) {
    await sqliteRun("ROLLBACK");
    throw error;
  }
}

async function initUserStore() {
  if (!PG_ENABLED) {
    return getUserStoreState();
  }

  if (!initPromise) {
    initPromise = (async () => {
      await ensurePgSchema();
      await bootstrapPgUsersFromSqlite();
      await syncMirrorIfNeeded(true);
      return getUserStoreState();
    })();
  }

  return initPromise;
}

function getUserStoreState() {
  return {
    provider: PG_ENABLED ? "postgres" : "sqlite",
    persistent: PG_ENABLED,
    mirror: PG_ENABLED ? "sqlite" : null,
  };
}

