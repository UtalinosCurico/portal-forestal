async function updateUserRecord(userId, changes = {}) {
  if (!PG_ENABLED) {
    throw new HttpError(500, "updateUserRecord requiere PostgreSQL habilitado");
  }

  const fields = [];
  const params = [];

  if (Object.prototype.hasOwnProperty.call(changes, "nombre")) {
    params.push(normalizeText(changes.nombre));
    fields.push(`nombre = $${params.length}`);
  }
  if (Object.prototype.hasOwnProperty.call(changes, "email")) {
    params.push(normalizeEmail(changes.email));
    fields.push(`email = $${params.length}`);
  }
  if (Object.prototype.hasOwnProperty.call(changes, "password_hash")) {
    params.push(changes.password_hash);
    fields.push(`password_hash = $${params.length}`);
  }
  if (Object.prototype.hasOwnProperty.call(changes, "rol")) {
    params.push(normalizeRole(changes.rol));
    fields.push(`rol = $${params.length}`);
  }
  if (Object.prototype.hasOwnProperty.call(changes, "equipo_id")) {
    params.push(changes.equipo_id ?? null);
    fields.push(`equipo_id = $${params.length}`);
  }
  if (Object.prototype.hasOwnProperty.call(changes, "activo")) {
    params.push(Boolean(changes.activo));
    fields.push(`activo = $${params.length}`);
  }
  if (Object.prototype.hasOwnProperty.call(changes, "archivado")) {
    params.push(Boolean(changes.archivado));
    fields.push(`archivado = $${params.length}`);
  }
  if (Object.prototype.hasOwnProperty.call(changes, "fecha_archivado")) {
    params.push(changes.fecha_archivado || null);
    fields.push(`fecha_archivado = $${params.length}`);
  }
  if (Object.prototype.hasOwnProperty.call(changes, "archivado_por")) {
    params.push(changes.archivado_por ?? null);
    fields.push(`archivado_por = $${params.length}`);
  }

  if (!fields.length) {
    return findUserById(userId);
  }

  params.push(Number(userId));
  const pg = getPool();
  const { rows } = await pg.query(
    `
      UPDATE usuarios_auth
      SET ${fields.join(", ")}, updated_at = NOW()
      WHERE id = $${params.length}
      RETURNING
        id,
        nombre,
        email,
        password_hash,
        rol,
        equipo_id,
        activo,
        archivado,
        fecha_archivado,
        archivado_por,
        fecha_creacion
    `,
    params
  );

  if (!rows.length) {
    throw new HttpError(404, "Usuario no encontrado");
  }

  const user = mapBaseUser(rows[0], await loadEquiposMap());
  await syncSingleUserToSqlite(user);
  return user;
}

