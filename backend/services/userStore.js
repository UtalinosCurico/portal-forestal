async function listSqliteUsers(filters = {}) {
  const equiposMap = await loadEquiposMap();
  const estado = String(filters.estado || "activos").trim().toLowerCase();
  const rol = filters.rol ? normalizeRole(filters.rol) : "";
  const search = normalizeText(filters.q || filters.search || "").toLowerCase();
  const params = [];
  const conditions = [];

  if (estado === "activos") {
    conditions.push("u.archivado = 0 AND u.activo = 1");
  } else if (estado === "inactivos") {
    conditions.push("u.archivado = 0 AND u.activo = 0");
  } else if (estado === "archivados") {
    conditions.push("u.archivado = 1");
  }

  if (rol) {
    params.push(rol);
    conditions.push(`u.rol = ?`);
  }

  if (search) {
    const searchValue = `%${search}%`;
    params.push(searchValue, searchValue, searchValue, searchValue);
    conditions.push(`(
      LOWER(COALESCE(u.nombre, '')) LIKE ?
      OR LOWER(COALESCE(u.email, '')) LIKE ?
      OR LOWER(COALESCE(u.rol, '')) LIKE ?
      OR LOWER(COALESCE(e.nombre_equipo, '')) LIKE ?
    )`);
  }

  const rows = await sqliteAll(
    `
      SELECT
        u.id,
        u.nombre,
        u.email,
        u.password_hash,
        u.rol,
        u.equipo_id,
        u.activo,
        COALESCE(u.archivado, 0) AS archivado,
        u.fecha_archivado,
        u.archivado_por,
        u.fecha_creacion,
        e.nombre_equipo AS equipo_nombre
      FROM usuarios u
      LEFT JOIN equipos e ON e.id = u.equipo_id
      ${conditions.length ? `WHERE ${conditions.join(" AND ")}` : ""}
      ORDER BY u.archivado ASC, u.activo DESC, u.id ASC
    `,
    params
  );

  return rows.map((row) => mapBaseUser(row, equiposMap));
}

function buildPgWhere(filters = {}) {
  const estado = String(filters.estado || "activos").trim().toLowerCase();
  const rol = filters.rol ? normalizeRole(filters.rol) : "";
  const conditions = [];
  const params = [];

  if (estado === "activos") {
    conditions.push("archivado = FALSE AND activo = TRUE");
  } else if (estado === "inactivos") {
    conditions.push("archivado = FALSE AND activo = FALSE");
  } else if (estado === "archivados") {
    conditions.push("archivado = TRUE");
  }

  if (rol) {
    params.push(rol);
    conditions.push(`rol = $${params.length}`);
  }

  return {
    where: conditions.length ? `WHERE ${conditions.join(" AND ")}` : "",
    params,
  };
}

