async function listUsers(filters = {}) {
  if (!PG_ENABLED) {
    return listSqliteUsers(filters);
  }

  const search = normalizeText(filters.q || filters.search || "").toLowerCase();
  const pg = getPool();
  const where = buildPgWhere(filters);
  const { rows } = await pg.query(
    `
      SELECT
        id,
        nombre,
        email,
        password_hash,
        rol,
        equipo_id,
        activo,
        archivado,
        fecha_archivado,
        archivado_por,
        fecha_creacion
      FROM usuarios_auth
      ${where.where}
      ORDER BY archivado ASC, activo DESC, id ASC
    `,
    where.params
  );

  const equiposMap = await loadEquiposMap();
  const mappedUsers = rows.map((row) => mapBaseUser(row, equiposMap));

  if (!search) {
    return mappedUsers;
  }

  return mappedUsers.filter((user) =>
    [user.nombre, user.email, user.rol, user.equipo_nombre]
      .filter(Boolean)
      .some((value) => String(value).toLowerCase().includes(search))
  );
}

async function countOtherActiveAdmins(excludedUserId) {
  if (!PG_ENABLED) {
    const row = await sqliteGet(
      `
        SELECT COUNT(*) AS total
        FROM usuarios
        WHERE rol = ?
          AND activo = 1
          AND COALESCE(archivado, 0) = 0
          AND id <> ?
      `,
      ["ADMIN", Number(excludedUserId)]
    );
    return Number(row?.total || 0);
  }

  const pg = getPool();
  const { rows } = await pg.query(
    `
      SELECT COUNT(*)::int AS total
      FROM usuarios_auth
      WHERE rol = $1
        AND activo = TRUE
        AND archivado = FALSE
        AND id <> $2
    `,
    ["ADMIN", Number(excludedUserId)]
  );

  return Number(rows[0]?.total || 0);
}

async function emailExists(email, excludeUserId = null) {
  const normalizedEmail = normalizeEmail(email);
  if (!PG_ENABLED) {
    const row = excludeUserId
      ? await sqliteGet(
          "SELECT id FROM usuarios WHERE LOWER(TRIM(email)) = ? AND id <> ?",
          [normalizedEmail, Number(excludeUserId)]
        )
      : await sqliteGet("SELECT id FROM usuarios WHERE LOWER(TRIM(email)) = ?", [normalizedEmail]);
    return Boolean(row);
  }

  const pg = getPool();
  const { rows } = excludeUserId
    ? await pg.query(
        "SELECT id FROM usuarios_auth WHERE LOWER(BTRIM(email)) = $1 AND id <> $2 LIMIT 1",
        [normalizedEmail, Number(excludeUserId)]
      )
    : await pg.query("SELECT id FROM usuarios_auth WHERE LOWER(BTRIM(email)) = $1 LIMIT 1", [normalizedEmail]);

  return rows.length > 0;
}

